menu = input("Введите пункт меню: напиток, суп, десерт: ")
value = None
if menu == "напиток":
    value = input("Выберите напиток: чай, кофе, сок: ")
elif menu == "суп":
    value = input("Выберите суп: борщ, щи, суп-пюре: ")
elif menu == "десерт":
    value = input("Выберите десерт: торт, мороженое, фрукты: ")
else:
    print("Такого пункта меню нет.")

if value is not None:
    match value:
        case "чай":
            print("Сумма к оплате: 50 рублей")
        case "кофе":
            print("Сумма к оплате: 70 рублей")
        case "сок":
            print("Сумма к оплате: 60 рублей")
        case "борщ":
            print("Сумма к оплате: 120 рублей")
        case "щи":
            print("Сумма к оплате: 100 рублей")
        case "суп-пюре":
            print("Сумма к оплате: 110 рублей")
        case "торт":
            print("Сумма к оплате: 150 рублей")
        case "мороженое":
            print("Сумма к оплате: 90 рублей")
        case "фрукты":
            print("Сумма к оплате: 80 рублей")
        case _:
            print("Такого блюда нет в меню.")
